def search_offline(query: str) -> list:
    """
    Search city database. Returns list of matches:
    [(full_name, lat, lon, country), ...]
    """
    q = query.strip().lower()
    results = []

    # Exact match first
    if q in SUNDALAND_CITIES:
        v = SUNDALAND_CITIES[q]
        results.append((v[3], v[0], v[1], v[2]))
        return results

    # Partial match
    for key, val in SUNDALAND_CITIES.items():
        if q in key or key in q:
            results.append((val[3], val[0], val[1], val[2]))

    # Sort by relevance (shorter key = more exact)
    results.sort(key=lambda x: len(x[0]))
    return results[:5]   # max 5 suggestions
